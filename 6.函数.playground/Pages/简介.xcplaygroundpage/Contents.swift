//: 函数
//:
//: ----
//:  - 函数是一段代码块，执行一个特定任务。且有响亮、会意的名称。
//:  - 使用函数叫“call”，调用、召唤之意。
//:  - 就像公式一样，使用函数一般需要提供参数。
//:  - 函数一般有一个或多个返回值，即执行结果。
//:  - 函数都有类型，包含参数和返回结果的类型。
//:  - 你可能之前就听过调用“方法”，方法就是函数。
